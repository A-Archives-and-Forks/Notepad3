# -----------------------------------------------------------------------------

MSG = "Hello world!"

def main():
	""" main function """
	print(f"{MSG}")

# -----------------------------------------------------------------------------

def pause():
	import os
	import sys
	if sys.platform == 'win32':
		os.system('pause')
	else:
		input('Press any key to continue...')
		
# -----------------------------------------------------------------------------

if __name__ == "__main__":
	try:
		main()
	finally:
		pause()

